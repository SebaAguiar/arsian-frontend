/****************************************************************************************************************************************************
 * * IMPORTS
 ****************************************************************************************************************************************************/
import { create } from 'zustand';
import { combine } from 'zustand/middleware';

/****************************************************************************************************************************************************
 * * TYPES - INTERFACES - CLASES
 ****************************************************************************************************************************************************/
interface ISideState {
  isSideActive: boolean;
  toggleSide: () => void;
}

/****************************************************************************************************************************************************
 * * DECLARATIONS
 ****************************************************************************************************************************************************/
/****************************************************************************************************************************************************
 * * FUNCTIONS
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * * EXPORTS
 ****************************************************************************************************************************************************/
export const useSideState = create<ISideState>((set) => ({
  isSideActive: false,
  toggleSide: () => set((state) => ({ isSideActive: !state.isSideActive })),
}));
