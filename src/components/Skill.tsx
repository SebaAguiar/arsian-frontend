/****************************************************************************************************************************************************
 * * IMPORTS
 ****************************************************************************************************************************************************/

import { ISkill } from '@/types';
import Image from 'next/image';
import React from 'react';
import styles from '../styles/skill.module.css';

/****************************************************************************************************************************************************
 * * TYPES - INTERFACES - CLASES
 ****************************************************************************************************************************************************/
interface ISkillProps {
  skills: ISkill[];
  key: number;
}
/****************************************************************************************************************************************************
 * * DECLARATIONS
 ****************************************************************************************************************************************************/

/****************************************************************************************************************************************************
 * * FUNCTIONS
 ****************************************************************************************************************************************************/
const Skill: React.FC<ISkillProps> = ({ skills, key }) => {
  return (
    <>
      {skills.map((s: ISkill, index) => (
        <div
          key={index}
          className='group m-1 flex h-max w-max flex-col items-center justify-center'
        >
          <Image
            id={styles.skillImage}
            width={100}
            height={100}
            className='text-dark-white m-2 h-14 w-14 lg:m-4'
            src={s.image}
            alt={s.name}
          />
        </div>
      ))}
    </>
  );
};

/****************************************************************************************************************************************************
 * * EXPORTS
 ****************************************************************************************************************************************************/
export default Skill;
